package com.clement.poll.dto;

public class ParticipationStats {
	Integer nbChild;

	Integer nbWomen;

	Integer nbVegetarian;

	public Integer getNbChild() {
		return nbChild;
	}

	public void setNbChild(Integer nbChild) {
		this.nbChild = nbChild;
	}

	public Integer getNbWomen() {
		return nbWomen;
	}

	public void setNbWomen(Integer nbWomen) {
		this.nbWomen = nbWomen;
	}

	public Integer getNbVegetarian() {
		return nbVegetarian;
	}

	public void setNbVegetarian(Integer nbVegetarian) {
		this.nbVegetarian = nbVegetarian;
	}

}
